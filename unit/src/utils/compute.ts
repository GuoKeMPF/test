
/**
 * 
 * @param number1 number 1
 * @param number2 number 2
 * @returns number1 + number2
 */
export const add = (number1: number, number2: number) => number1 + number2;


/**
 * 
 * @param number1 number 1
 * @param number2 number 2
 * @returns number1 - number2
 */
export const reduce = (number1: number, number2: number) => number1 - number2;




